        </pre>
        </h4>
</div>
<p>
Getting hands-on lab experience as an undergraduate is a great way to determine if research is the right path for you. Our lab often has several undergraduate students working with specific graduate students or postdocs on research projects. Students interested in working in the lab should send an email containing research interests and a resume so we can determine if our lab will be a good fit. Undergrads should expect to spend 6-12 hours a week in the lab throughout the semester. Students are encouraged to earn course credit (X490) and consider developing an honor's thesis based on their research projects. 
</p>


