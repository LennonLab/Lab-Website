<p>
Microorganisms are the most abundant and diverse life forms on Earth. They attain high population densities, have fast reproductive rates, and evolve rapidly to changes in their environment. Moreover, microbes carry out important functions, including nutrient cycling, trace gas flux, and carbon sequestration, which are important for the stability of natural and managed ecosystems. 
</p>

<p>We study the ecology and evolution of microbial communities. We are interested in the biotic and abiotic factors that generate and maintain microbial biodiversity. In turn, we seek to understand the implications of microbial diversity for ecosystem functioning. We conduct research in terrestrial and aquatic habitats, and use a variety of tools including molecular biology, simulation modeling, laboratory experiments, field surveys, and whole ecosystem manipulations in natural and managed ecosystems. </p>

